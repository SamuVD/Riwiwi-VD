# # Operadores Relacionales
Mayor_Que = 10 > 20
print(Mayor_Que) 

Menor_Que = 10 < 20 
print(Menor_Que)

Mayor_O_Igual_Que = 10 >= 20
print(Mayor_O_Igual_Que)

Menor_O_Igual_Que = 10 <= 20 
print(Menor_O_Igual_Que)

Diferente = 10 != 20 
print(Diferente)

Igual = 10 == 20 
print(Igual) 

